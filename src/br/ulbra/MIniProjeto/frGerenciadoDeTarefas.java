    private void btnExcluirTarefaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirTarefaActionPerformed
        btnListarTarefas.doClick();
        int numeroDaLista = Integer.parseInt(JOptionPane.showInputDialog(null, "Informe o número da tarefa que deseja excluir :"));
        if (JOptionPane.showConfirmDialog(null, "Tem certeza que quer excluir essa tarefa ? ", "confirme", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
            gerenciar.excluirTarefa((numeroDaLista - 1));
            btnListarTarefas.doClick();
        } else {
            JOptionPane.showMessageDialog(null, "Exclusão cancelada ");
        }
    }//GEN-LAST:event_btnExcluirTarefaActionPerformed

