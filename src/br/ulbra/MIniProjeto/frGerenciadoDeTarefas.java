    private void btnEditarTarefaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarTarefaActionPerformed
        btnListarTarefas.doClick();
        int cod = Integer.parseInt(JOptionPane.showInputDialog(null, "Informe o número da tarefa que deseja alterar :"))-1;
        if (cod > gerenciar.retornarTamanho() || cod < 0) {
            JOptionPane.showMessageDialog(null, "A tarefa não existe");
        } else {
            String novaTarefa = JOptionPane.showInputDialog(null, "Informe a nova tarefa :");
            gerenciar.alterar(cod, novaTarefa);
            btnListarTarefas.doClick();
        }
    }//GEN-LAST:event_btnEditarTarefaActionPerformed

